# Integer variables
int1 = 30
int2 = -70

sum = int1 + int2
sub = int1 - int2

print(sum)
print(sub)
print("Esta es la suma: ", sum)
print("Esta es la resta: ", sub)

# Allows you to embed variables and expressions within strings
print(f"Esta es la suma formateada: {sum}")
print(f"Esta es la resta formateada: {sub}")


# Float variables
float1 = -15.8
sum_f_i = float1 + int1
print(f"La suma de int1 y float1 es: {sum_f_i}")

# Creating arrays
array1 = [1, 5, 4, 3, 7, -8, 9, -10]
array2 = [2.3, 5.8, 4.5, 7.9, 2.0]
print(array1)
print(array1[0])
print(array1[1])
print(array1[2])
print(array2)

# Creating Lists
list1 = [2, "three", 5.0, True]
list2 = [4.0, 9.55, False, True, list1, 100]
print(list1)
print(list2)